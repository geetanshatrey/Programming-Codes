class SwitchCase {
    public static void main(String args[]) {
        int x = 3;
        switch(x) {
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            case 3:
                System.out.println("3");
                break;
            default:
                System.out.println("bigger than 3");
                break;
        } 
    }
}
